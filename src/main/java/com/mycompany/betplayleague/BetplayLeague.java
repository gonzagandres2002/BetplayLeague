    private void btnSimulateTournamentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimulateTournamentActionPerformed

        String results = "";

        if (round == 19) {
            results = "The tournament has ended \n Winner: " + league.getTeams().get(0);
        }

        for (int i = round; i < 19; i++) {
            results += "MatchDay " + (round + 1) + ":\n";
            results += MatchSimulator.simulateMatches(league, round);
            results += "\n";
            round++;
        }
        tournament = new Tournament(results);

        tournament.setSize(750, 430);
        tournament.setLocation(0, 0);

        content.removeAll();
        content.add(tournament, BorderLayout.CENTER);
        content.revalidate();
        content.repaint();
    }//GEN-LAST:event_btnSimulateTournamentActionPerformed

