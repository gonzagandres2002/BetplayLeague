    private void btnSimulateDayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimulateDayActionPerformed
        String results = "";

        if (round == 19) {
            results = "The tournament has ended \n Winner: " + league.getTeams().get(0);
        } else {
            results = "MatchDay " + (round + 1) + ":\n";
            results += MatchSimulator.simulateMatches(league, round);
            round++;
        }

        gameDay = new GameDay(results);
        gameDay.setSize(750, 430);
        gameDay.setLocation(0, 0);

        content.removeAll();
        content.add(gameDay, BorderLayout.CENTER);
        content.revalidate();
        content.repaint();
    }//GEN-LAST:event_btnSimulateDayActionPerformed

